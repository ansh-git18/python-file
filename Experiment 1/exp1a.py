#demonstrating diffrent data types in python

#Integer(int)
age=25
print("integer type value: ",age)
print("type of 'age':",type(age))

#floating point number (float)
height=5.9
print("in float value :",height)
print("type of 'height'",type(height))

#boolean (bool)
is_student=True
print("in boolean value:",is_student)
print("type of 'is_student':",type(is_student))

#string(str)
name="alice"
print("in string value:",name)
print("type of 'alice':",type(name))